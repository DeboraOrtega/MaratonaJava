package acadamy.devdojo.maratonajava.introducao;

public class OperadoresLogicos {
    public static void main(String[] args) {
        int idade = 10;
        float salario = 1000;

        System.out.println(idade >=10 && salario >= 2000);
        System.out.println(idade >=10 || salario >= 2000);
        System.out.println(idade >=10 && salario >= 1000);
    }
}
